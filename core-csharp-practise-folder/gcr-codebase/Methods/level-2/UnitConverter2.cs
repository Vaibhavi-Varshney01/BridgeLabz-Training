using System;
class UnitConverter2{
    public static double ConvertYardsToFeet(double y) => y * 3;
    public static double ConvertFeetToYards(double f) => f * 0.333333;
    public static double ConvertMetersToInches(double m) => m * 39.3701;
    public static double ConvertInchesToMeters(double i) => i * 0.0254;
    public static double ConvertInchesToCm(double i) => i * 2.54;
}
